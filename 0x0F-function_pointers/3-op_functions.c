#include "3-calc.h"

/**
 * op_add - function for sum of two integers
 * @x: first integer
 * @y: second integer
 *
 * Return: always 0
 */
int op_add(int x, int y)
{
	return (x + y);
}

/**
 * op_sub - function for difference of two integers
 * @x: first integer
 * @y: second integer
 *
 * Return: always 0
 */
int op_sub(int x, int y)
{
	return (x - y);
}

/**
 * op_mul - function for product of two integers
 * @x: first integer
 * @y: second integer
 *
 * Return: always 0
 */
int op_mul(int x, int y)
{
	return (x * y);
}

/**
 * op_div - function for division of two integers
 * @x: first integer
 * @y: second integer
 *
 * Return: always 0
 */
int op_div(int x, int y)
{
	return (x / y);
}

/**
 * op_rem - function for remainder of the division of two integers
 * @x: first integer
 * @y: second integer
 *
 * Return: always 0
 */
int op_rem(int x, int y)
{
	return (x % y);
}
